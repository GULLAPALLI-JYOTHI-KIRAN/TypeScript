/* JavaScript and TypeScript Primitives
The most basic types in TypeScript are called primitives.

These types form the building blocks of more complex types in your applications.

TypeScript includes all JavaScript primitives plus additional type features. */

//Boolean
let statu :boolean=true;
console.log(statu);


//Number
let num:number=20;
console.log(num);

let dec:number=2.134;
console.log(dec);


let hex:number=0x243aed;
console.log(hex);

let bin:number=0b1010;
console.log(bin);

let octal:number=0o123;
console.log(octal);


//BigInt
let bigNum:bigint=1927373n;
console.log(bigNum);


//String
let Fullname:string="John Deo";
console.log(Fullname);


//symbol
const unq:unique symbol=Symbol("12ded");
console.log(unq);




